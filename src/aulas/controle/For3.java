package aulas.controle;

public class For3 {

    public static void main(String[] args) {
        for (int x = 0; x < 10; x++) {
            for (int y = 0; y < 10; y++) {
                System.out.printf(x + ":" + y +", ");
            }
            System.out.println();
        }
    }
}
